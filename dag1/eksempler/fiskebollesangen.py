"""
Fiskebollesangen
"""

vers = 1

while vers <= 100:
    print("Fiskebollen lengter etter havet,")
    print("for havet er fiskebollens hjem,")
    print(f"dette er det {vers}. verset")
    print(f"Da er det bare {100 - vers} vers igjen.")
    print()
    vers = vers + 1